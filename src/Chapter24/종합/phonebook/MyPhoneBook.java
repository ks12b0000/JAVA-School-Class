package Chapter24.종합.phonebook;

public class MyPhoneBook {

	String name;
	String phoneNumber;
	String email;
	
	public MyPhoneBook(String name, String phoneNumber) {
		this.name = name;
		this.phoneNumber = phoneNumber;
	}
	public MyPhoneBook(String name, String phoneNumber,String email) {
		this.name = name;
		this.phoneNumber = phoneNumber;
		this.email = email;
	}
	
	
	
}
